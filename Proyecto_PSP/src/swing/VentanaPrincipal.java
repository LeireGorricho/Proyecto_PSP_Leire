    private void darColor(JPanel pane, JPanel borde, JLabel label) {
        pane.setBackground(new Color(241, 217, 228));
        borde.setBackground(new Color(255,255,255));
        label.setForeground(new Color(51, 51, 51));
    }
    
    private void devolverColor(JPanel pane, JPanel borde, JLabel label) {
        pane.setBackground(new Color(153, 0, 102));
        borde.setBackground(new Color(153, 0, 102));
        label.setForeground(Color.WHITE);
    }

